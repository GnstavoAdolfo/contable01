    private boolean validar(){
        boolean validado = true;
        String dni = elemento_dni.getText();
        String descripcion = elemento_nombre.getText();
        //String tipo;
        Integer estado;
        if(!dni.trim().equals("") && !descripcion.trim().equals("")){
            try{
                estado = Integer.parseInt(elemento_estado.getSelectedItem().toString());
                //tipo = elemento_tipo.getSelectedItem().toString();
            }catch(Exception ex){
                validado = false;
            }
        }else{
            validado = false;
        }
        return validado;
    }
    
    private void guardar(){
        String dni = elemento_dni.getText();
        String descripcion = elemento_nombre.getText();
        Integer estado = Integer.parseInt(elemento_estado.getSelectedItem().toString());
        String tipo = elemento_tipo.getSelectedItem().toString();
        
        //this.tarea.setUno();
        this.tarea.setDos(dni);
        this.tarea.setTres(descripcion);
        this.tarea.setCuatro(estado);
        this.tarea.setCinco(tipo);
        try{
            this.tareas_servicio.guardar(Conexion.obtener(), this.tarea);
            vista_guardar_cliente.this.dispose();
            vista_cliente vista = new vista_cliente();
            vista.setVisible(true);
            vista.setLocationRelativeTo(null);
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se ha podido guardar el registro.");
        }catch(ClassNotFoundException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se ha podido guardar el registro.");
        }
    }
    
